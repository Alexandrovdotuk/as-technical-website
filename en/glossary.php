        <section id="rauch">
            <h1>Smoking Like a Pro</h1>
            <p>Give your products the perfect smoky flavor! Learn how classic and modern smoking methods can enhance your butchery.</p>

            <h2>1. Basics of Smoking</h2>
            <p>Smoking is a time-tested method for refining and preserving food.</p>
            <ul>
                <li><strong>Goals:</strong>
                    <ul>
                        <li><strong>Flavoring:</strong> Smoke imparts unique aromas.</li>
                        <li><strong>Preservation:</strong> Inhibits microorganisms.</li>
                        <li><strong>Coloring:</strong> Golden brown to deep red hues.</li>
                    </ul>
                </li>
            </ul>

            <h2>2. Technical Aspects</h2>
            <h3>Physical Processes</h3>
            <ul>
                <li><strong>Smoke Generation:</strong> Incomplete combustion of wood produces aromatic compounds.</li>
                <li><strong>Diffusion:</strong> Smoke penetrates the food.</li>
                <li><strong>Deposition:</strong> Aromatic compounds settle on the surface.</li>
            </ul>
            <h3>Smoke Components</h3>
            <ul>
                <li><strong>Phenols:</strong> Flavor and preservation.</li>
                <li><strong>Carbonyl Compounds:</strong> Golden color.</li>
                <li><strong>Organic Acids:</strong> Preservative, aromatic.</li>
                <li><strong>Tar Substances:</strong> Bitter, to be avoided.</li>
            </ul>

            <h2>3. Smoking Methods</h2>
            <h3>Cold Smoking</h3>
            <ul>
                <li><strong>Temperature:</strong> 15–25 °C.</li>
                <li><strong>Goal:</strong> Long shelf life, subtle flavor.</li>
                <li><strong>Products:</strong> Raw ham, salmon, salami.</li>
            </ul>
            <h3>Warm Smoking</h3>
            <ul>
                <li><strong>Temperature:</strong> 25–50 °C.</li>
                <li><strong>Goal:</strong> Mild smoky flavor.</li>
                <li><strong>Products:</strong> Sausages, cheese.</li>
            </ul>
            <h3>Hot Smoking</h3>
            <ul>
                <li><strong>Temperature:</strong> 50–85 °C.</li>
                <li><strong>Goal:</strong> Intense smoky note.</li>
                <li><strong>Products:</strong> Trout, poultry.</li>
            </ul>
            <h3>Liquid Smoke</h3>
            <p>Liquid smoke is used industrially to efficiently create flavor and color.</p>

            <h2>4. Material Science</h2>
            <h3>Wood Selection</h3>
            <ul>
                <li><strong>Hardwoods:</strong> Beech (mild).</li>
                <li><strong>Fruitwoods:</strong> Apple, cherry (fruity).</li>
                <li><strong>Oak:</strong> Robust, savory.</li>
                <li><strong>Herbs:</strong> Juniper, rosemary.</li>
            </ul>
            <h3>Unsuitable</h3>
            <ul>
                <li><strong>Softwoods:</strong> Resinous, unsuitable.</li>
            </ul>

            <h2>5. Professional Equipment</h2>
            <h3>Craft Smoking</h3>
            <ul>
                <li>Smoking Oven: For small batches.</li>
                <li>Smoking Cabinet: For medium-sized operations.</li>
            </ul>
            <h3>Industrial Smoking</h3>
            <ul>
                <li>Large Systems: Automated control.</li>
                <li>Combination Systems: Smoking, cooking, cooling.</li>
            </ul>

            <h2>6. Hygienic and Legal Requirements</h2>
            <ul>
                <li><strong>HACCP:</strong> Hazard analysis and critical control points.</li>
                <li><strong>Food Law:</strong> Limits for PAHs (EU Regulation).</li>
                <li><strong>Cleaning:</strong> Regular removal of tar deposits.</li>
            </ul>

            <h2>7. Quality Characteristics</h2>
            <ul>
                <li><strong>Smoke Pattern:</strong> Even, mild.</li>
                <li><strong>Surface:</strong> Glossy, dry.</li>
                <li><strong>Color:</strong> Appetizing, uniform.</li>
                <li><strong>Flavor:</strong> Harmonious.</li>
            </ul>

            <h2>8. Innovations</h2>
            <ul>
                <li><strong>Hybrid Smoking:</strong> Combined with sous-vide.</li>
                <li><strong>Digitalization:</strong> App-controlled systems.</li>
                <li><strong>Sustainability:</strong> Residual wood or biomass.</li>
            </ul>

            <h2>9. Typical Products</h2>
            <ul>
                <li><strong>Meat:</strong> Black Forest ham, salami.</li>
                <li><strong>Fish:</strong> Salmon, trout.</li>
                <li><strong>Cheese:</strong> Smoked cheese.</li>
                <li><strong>Innovative:</strong> Smoked vegetables, vegan products.</li>
            </ul>
        </section>

