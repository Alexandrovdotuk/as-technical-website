        <section id="steak">
            <h1>Exceptional Steak Cuts</h1>
            <p>Discover the variety of steak cuts – from Flat Iron to Picanha – and learn how to prepare each cut perfectly.</p>
            <img src="../lexikon/steak-diagram.jpg" alt="Steak Cuts Diagram" class="lexikon-image" loading="lazy">

            <h2>1. Flat Iron Steak</h2>
            <p>A tender highlight from the shoulder, ideal for quick searing.</p>
            <ul>
                <li><strong>Origin:</strong> Chuck (shoulder blade).</li>
                <li><strong>Feature:</strong> Tender, flat cut after removing the tendon.</li>
                <li><strong>Flavor:</strong> Intense and aromatic.</li>
                <li><strong>Preparation:</strong> Grill or sear to medium-rare.</li>
            </ul>

            <h2>2. Spider Steak (Oyster Blade)</h2>
            <p>A rare, marbled cut with a unique texture.</p>
            <ul>
                <li><strong>Origin:</strong> Hip bone area.</li>
                <li><strong>Feature:</strong> Spiderweb-like fat marbling.</li>
                <li><strong>Flavor:</strong> Juicy, aromatic.</li>
                <li><strong>Preparation:</strong> Grill briefly, avoid overcooking.</li>
            </ul>

            <h2>3. Bavette (Flap Steak)</h2>
            <p>Tender and fibrous, perfect for quick preparation.</p>
            <ul>
                <li><strong>Origin:</strong> Flank.</li>
                <li><strong>Feature:</strong> Similar to flank steak but more tender.</li>
                <li><strong>Flavor:</strong> Intense, juicy.</li>
                <li><strong>Preparation:</strong> Grill hot, slice against the grain.</li>
            </ul>

            <h2>4. Denver Cut</h2>
            <p>Marbled and juicy, ideal for versatile preparation.</p>
            <ul>
                <li><strong>Origin:</strong> Shoulder (chuck roll).</li>
                <li><strong>Feature:</strong> Tender despite marbling.</li>
                <li><strong>Flavor:</strong> Robust, full-bodied.</li>
                <li><strong>Preparation:</strong> Grill, pan, or sous-vide.</li>
            </ul>

            <h2>5. Teres Major Steak</h2>
            <p>A cost-effective alternative to tenderloin.</p>
            <ul>
                <li><strong>Origin:</strong> Shoulder (petite tender).</li>
                <li><strong>Feature:</strong> Very tender, cheaper than tenderloin.</li>
                <li><strong>Flavor:</strong> Robust, meaty.</li>
                <li><strong>Preparation:</strong> Sear or grill briefly.</li>
            </ul>

            <h2>6. Tri-Tip</h2>
            <p>An American classic with a triangular shape.</p>
            <ul>
                <li><strong>Origin:</strong> Bottom sirloin.</li>
                <li><strong>Feature:</strong> Popular in California.</li>
                <li><strong>Flavor:</strong> Nutty, juicy.</li>
                <li><strong>Preparation:</strong> Grill or smoke.</li>
            </ul>

            <h2>7. Picanha (Top Sirloin Cap)</h2>
            <p>A Brazilian favorite with a fat cap.</p>
            <ul>
                <li><strong>Origin:</strong> Rear back.</li>
                <li><strong>Feature:</strong> Juicy due to fat cap.</li>
                <li><strong>Flavor:</strong> Buttery, aromatic.</li>
                <li><strong>Preparation:</strong> Roast over an open flame.</li>
            </ul>

            <h2>8. Oyster Blade Steak</h2>
            <p>Aromatic and versatile from the shoulder.</p>
            <ul>
                <li><strong>Origin:</strong> Chuck.</li>
                <li><strong>Feature:</strong> Classic with tendon.</li>
                <li><strong>Flavor:</strong> Lightly marbled.</li>
                <li><strong>Preparation:</strong> Sear or braise.</li>
            </ul>

            <h2>9. Hanger Steak (Butcher’s Steak)</h2>
            <p>Rare and intense in flavor.</p>
            <ul>
                <li><strong>Origin:</strong> Diaphragm.</li>
                <li><strong>Feature:</strong> Only one piece per animal.</li>
                <li><strong>Flavor:</strong> Gamey, intense.</li>
                <li><strong>Preparation:</strong> Grill to medium-rare.</li>
            </ul>

            <h2>10. Chuck Eye Steak</h2>
            <p>An affordable alternative to ribeye.</p>
            <ul>
                <li><strong>Origin:</strong> Front back.</li>
                <li><strong>Feature:</strong> Similar to ribeye.</li>
                <li><strong>Flavor:</strong> Robust, juicy.</li>
                <li><strong>Preparation:</strong> Grill or sear.</li>
            </ul>

            <h2>11. Beef Ribs (Short Ribs)</h2>
            <p>Visually impressive „Dino Steaks.“</p>
            <ul>
                <li><strong>Origin:</strong> Rib area.</li>
                <li><strong>Feature:</strong> Long-cut ribs.</li>
                <li><strong>Flavor:</strong> Juicy due to fat structure.</li>
                <li><strong>Preparation:</strong> Grill or smoke.</li>
            </ul>

            <h2>12. Cap of Ribeye</h2>
            <p>A premium cut with a buttery texture.</p>
            <ul>
                <li><strong>Origin:</strong> Upper ribeye section.</li>
                <li><strong>Feature:</strong> Extremely tender.</li>
                <li><strong>Flavor:</strong> Full-bodied.</li>
                <li><strong>Preparation:</strong> Sear quickly.</li>
            </ul>

            <h2>13. Tomahawk Steak</h2>
            <p>A showpiece for special occasions.</p>
            <ul>
                <li><strong>Origin:</strong> Ribeye with a long bone.</li>
                <li><strong>Feature:</strong> Impressive appearance.</li>
                <li><strong>Flavor:</strong> Juicy, intense.</li>
                <li><strong>Preparation:</strong> Grill or smoker.</li>
            </ul>

            <h2>14. Flat Meat (Secreto)</h2>
            <p>Marbled and tender, a hidden gem.</p>
            <ul>
                <li><strong>Origin:</strong> Intercostal area.</li>
                <li><strong>Feature:</strong> Highly aromatic.</li>
                <li><strong>Flavor:</strong> Full-bodied.</li>
                <li><strong>Preparation:</strong> Sear briefly, slice thinly.</li>
            </ul>
