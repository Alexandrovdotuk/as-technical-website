        <section id="flussigrauch">
            <h1>Liquid Smoke: Efficiency Meets Flavor</h1>
            <p>Liquid smoke revolutionizes food processing. Learn how this innovative product saves time and costs.</p>

            <h2>1. Definition and Origin</h2>
            <p>Liquid smoke (smoke condensate) is produced through controlled combustion and condensation of wood smoke. It imparts a smoked flavor and color to food.</p>

            <h2>2. Technological Production</h2>
            <p>The production process involves several steps:</p>
            <ol>
                <li><strong>Smoke Generation:</strong> Wood (e.g., beech, oak) is burned at 300–400 °C.</li>
                <li><strong>Condensation:</strong> Smoke is condensed using water or cooling.</li>
                <li><strong>Filtration:</strong> Contaminants (soot, tar) are removed.</li>
                <li><strong>Standardization:</strong> Concentration for specific flavor profiles.</li>
            </ol>

            <h2>3. Chemical Composition</h2>
            <ul>
                <li><strong>Phenols:</strong> Flavor, antioxidant effect.</li>
                <li><strong>Carbonyl Compounds:</strong> Brown coloring.</li>
                <li><strong>Organic Acids:</strong> Preservation.</li>
                <li><strong>Aldehydes:</strong> Aromatic character.</li>
            </ul>

            <h2>4. Applications</h2>
            <h3>Meat Industry</h3>
            <p>Liquid smoke is injected into or applied to sausages, hams, or bacon.</p>
            <h3>Fish Industry</h3>
            <p>Shortens smoking time for salmon, mackerel, etc.</p>
            <h3>Cheese Production</h3>
            <p>Provides smoked cheese with uniform color and aroma.</p>

            <h2>5. Technical Advantages</h2>
            <h3>Hygiene</h3>
            <p>Free of tar and PAHs, safe for food.</p>
            <h3>Efficiency</h3>
            <p>Shorter production times, consistent results.</p>
            <h3>Flexibility</h3>
            <p>Precise dosing for various flavors.</p>
            <h3>Sustainability</h3>
            <p>Reduced wood and energy consumption.</p>

            <h2>6. Quality Requirements</h2>
            <ul>
                <li>Consistent composition.</li>
                <li>No toxic substances.</li>
                <li>Compliance with EU Regulation (EC) No. 2065/2003.</li>
            </ul>

            <h2>7. Application Technologies</h2>
            <h3>Coating Systems</h3>
            <p>Spray tunnels or coating systems.</p>
            <h3>Drum Method</h3>
            <p>For nuts or snacks.</p>
            <h3>Marinating Machines</h3>
            <p>In vacuum tumblers with spices.</p>

            <h2>8. Standards and Regulations</h2>
            <ul>
                <li><strong>EU Legislation:</strong> Strict quality requirements.</li>
                <li><strong>HACCP:</strong> Hazard analysis required.</li>
                <li><strong>Labeling:</strong> Must indicate „with smoke flavor.“</li>
            </ul>
