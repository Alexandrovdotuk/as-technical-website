<!-- Initialize Swiper -->
<script>
  // Funktion zur Anpassung der Höhe des Swipers
  function adjustSwiperHeight(swiper) {
    const activeSlide = swiper.slides[swiper.activeIndex];
    const img = activeSlide.querySelector('img');
    const p = activeSlide.querySelector('p');
    const imgHeight = img ? img.offsetHeight : 0;
    const pHeight = p ? p.offsetHeight : 0;
    const totalHeight = imgHeight + pHeight + 20; // 20px für Margin
    swiper.el.style.height = `${totalHeight}px`;
  }

  // Miniatur-Swiper 1
  const thumbSwiper1 = new Swiper('.thumb-swiper-1', {
    slidesPerView: 7,
    spaceBetween: 10,
    freeMode: true,
    watchSlidesProgress: true,
  });

  // Haupt-Swiper 1
  const mainSwiper1 = new Swiper('.main-swiper-1', {
    slidesPerView: 1,
    spaceBetween: 10,
    navigation: {
      nextEl: '.main-swiper-1 .swiper-button-next',
      prevEl: '.main-swiper-1 .swiper-button-prev',
    },
    thumbs: {
      swiper: thumbSwiper1,
    },
    on: {
      slideChange: function() {
        adjustSwiperHeight(this);
      },
      init: function() {
        adjustSwiperHeight(this); // Höhe beim Laden anpassen
      },
    },
  });

  // Miniatur-Swiper 2
  const thumbSwiper2 = new Swiper('.thumb-swiper-2', {
    slidesPerView: 6,
    spaceBetween: 10,
    freeMode: true,
    watchSlidesProgress: true,
  });

  // Haupt-Swiper 2
  const mainSwiper2 = new Swiper('.main-swiper-2', {
    slidesPerView: 1,
    spaceBetween: 10,
    navigation: {
      nextEl: '.main-swiper-2 .swiper-button-next',
      prevEl: '.main-swiper-2 .swiper-button-prev',
    },
    thumbs: {
      swiper: thumbSwiper2,
    },
    on: {
      slideChange: function() {
        adjustSwiperHeight(this);
      },
      init: function() {
        adjustSwiperHeight(this); // Höhe beim Laden anpassen
      },
    },
  });

  // Miniatur-Swiper 3
  const thumbSwiper3 = new Swiper('.thumb-swiper-3', {
    slidesPerView: 6,
    spaceBetween: 10,
    freeMode: true,
    watchSlidesProgress: true,
  });

  // Haupt-Swiper 3
  const mainSwiper3 = new Swiper('.main-swiper-3', {
    slidesPerView: 1,
    spaceBetween: 10,
    navigation: {
      nextEl: '.main-swiper-3 .swiper-button-next',
      prevEl: '.main-swiper-3 .swiper-button-prev',
    },
    thumbs: {
      swiper: thumbSwiper3, // Korrigierter Verweis auf thumbSwiper3
    },
    on: {
      slideChange: function() {
        adjustSwiperHeight(this);
      },
      init: function() {
        adjustSwiperHeight(this); // Höhe beim Laden anpassen
      },
    },
  });

  // Höhe beim Laden aller Bilder anpassen (nur einmal nötig)
  window.addEventListener('load', () => {
    adjustSwiperHeight(mainSwiper1);
    adjustSwiperHeight(mainSwiper2);
    adjustSwiperHeight(mainSwiper3); // Für Swiper 3 hinzugefügt
  });
</script>

