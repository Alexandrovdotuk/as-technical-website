        <section id="zerlegen">
            <h1>Die Kunst des Zerlegens in der Metzgerei</h1>
            <p>Beherrschen Sie das Zerlegen wie ein Profi! Dieser zentrale Schritt in der Fleischverarbeitung erfordert Präzision, Wissen und die richtigen Werkzeuge. Lernen Sie, wie Sie Fleisch optimal nutzen und Ihre Kunden begeistern.</p>

            <h2>1. Grundlagen des Zerlegens</h2>
            <p>Zerlegen bedeutet, ganze Tierkörper oder Teilstücke in verkaufsfertige Stücke zu schneiden. Es ist der erste Schritt nach der Schlachtung und kombiniert Handwerkskunst mit tiefem Verständnis für Fleischqualität.</p>
            <ul>
                <li><strong>Ziele:</strong>
                    <ul>
                        <li>Hochwertige Fleischstücke für den Verkauf gewinnen.</li>
                        <li>Den Tierkörper maximal nutzen, um Verschwendung zu vermeiden.</li>
                        <li>Fleisch für die Weiterverarbeitung (z. B. Wurstherstellung, Räuchern) vorbereiten.</li>
                    </ul>
                </li>
            </ul>

            <h2>2. Fachliche Anforderungen</h2>
            <h3>Anatomiekenntnisse</h3>
            <p>Ein Metzger muss die Anatomie des Tieres genau kennen:</p>
            <ul>
                <li><strong>Skelettsystem:</strong> Position von Knochen und Gelenken.</li>
                <li><strong>Muskulatur:</strong> Lage der Fleischstücke und Qualität (z. B. zart oder bindegewebsreich).</li>
                <li><strong>Organe:</strong> Fachgerechtes Entfernen von Innereien.</li>
            </ul>
            <h3>Qualitätsbewertung</h3>
            <p>Die Qualität des Fleisches wird anhand folgender Kriterien beurteilt:</p>
            <ul>
                <li>Farbe.</li>
                <li>Marmorierung (Fettanteil).</li>
                <li>Textur (Zartheit).</li>
                <li>Reifung des Fleisches.</li>
            </ul>
            <h3>Schnittführung</h3>
            <p>Perfekte Schnitte entlang von Muskelsträngen, Sehnen und Knochen minimieren Verluste und liefern hochwertige Teilstücke.</p>

            <h2>3. Schritte beim Zerlegen</h2>
            <h3>a) Vorbereitung</h3>
            <ul>
                <li><strong>Arbeitsplatz:</strong> Hygienisch einwandfreie Schneidebretter, Messer und Maschinen.</li>
                <li><strong>Werkzeuge:</strong> Zerlegemesser, Knochensäge, Hackbeil, Ausbeinmesser.</li>
                <li><strong>Kühlung:</strong> Fleisch bei 2–7 °C lagern.</li>
            </ul>
            <h3>b) Grobzerlegung</h3>
            <p>Der Tierkörper wird in Hauptteile zerlegt:</p>
            <ul>
                <li><strong>Rind:</strong> Vorderviertel, Hinterviertel, Rücken, Brust, Keule.</li>
                <li><strong>Schwein:</strong> Schulter, Rücken, Bauch, Schinken.</li>
                <li><strong>Lamm/Ziege:</strong> Rücken, Keule, Brust, Schulter.</li>
            </ul>
            <h3>c) Feinzerlegung</h3>
            <p>Grobteile werden in kleinere Stücke geschnitten:</p>
            <ul>
                <li><strong>Rinderrücken:</strong> Roastbeef, Filet, Entrecôte.</li>
                <li><strong>Schweineschulter:</strong> Gulasch, Hackfleisch.</li>
                <li><strong>Lammkeule:</strong> Braten, Steaks.</li>
            </ul>
            <h3>d) Portionierung</h3>
            <p>Fleisch wird je nach Kundenwunsch portioniert (z. B. Steaks, Koteletts, Schnitzel).</p>

            <h2>4. Werkzeuge und Maschinen</h2>
            <ul>
                <li><strong>Handwerkliche Werkzeuge:</strong>
                    <ul>
                        <li>Ausbeinmesser: Zum Herauslösen von Knochen.</li>
                        <li>Knochensäge: Für präzises Trennen von Knochen.</li>
                        <li>Hackbeil: Zum groben Zerkleinern.</li>
                        <li>Schärfgeräte: Für scharfe Schnittkanten.</li>
                    </ul>
                </li>
                <li><strong>Maschinelle Unterstützung:</strong>
                    <ul>
                        <li>Bandsägen.</li>
                        <li>Fleischwolf.</li>
                        <li>Vakuumverpackungsgeräte.</li>
                    </ul>
                </li>
            </ul>

            <h2>5. Fleischstücke und ihre Verwendung</h2>
            <h3>Rind</h3>
            <ul>
                <li><strong>Keule:</strong> Oberschale (Schnitzel), Unterschale (Rouladen), Tafelspitz (Sieden).</li>
                <li><strong>Rücken:</strong> Roastbeef (Steaks), Filet (edelstes Teilstück).</li>
                <li><strong>Brust:</strong> Brustkern (Suppenfleisch), Flache Brust (Schmorgerichte).</li>
            </ul>
            <h3>Schwein</h3>
            <ul>
                <li><strong>Schinken:</strong> Rohschinken, Kochschinken.</li>
                <li><strong>Rücken:</strong> Koteletts, Schweinefilet.</li>
                <li><strong>Bauch:</strong> Bauchspeck, Schweinebauch.</li>
            </ul>
            <h3>Lamm</h3>
            <ul>
                <li><strong>Keule:</strong> Braten oder Steaks.</li>
                <li><strong>Rücken:</strong> Lammkarree.</li>
                <li><strong>Schulter:</strong> Ragout oder Gulasch.</li>
            </ul>

            <h2>6. Hygienevorschriften</h2>
            <ul>
                <li><strong>Arbeitsplatz:</strong>
                    <ul>
                        <li>Regelmäßige Desinfektion von Arbeitsflächen und Werkzeugen.</li>
                        <li>Trennung von Roh- und verarbeitetem Fleisch.</li>
                    </ul>
                </li>
                <li><strong>Persönliche Hygiene:</strong>
                    <ul>
                        <li>Saubere Arbeitskleidung, Handschuhe, Kopfbedeckung.</li>
                    </ul>
                </li>
                <li><strong>Lebensmittelrecht:</strong> Einhaltung der EU-Verordnung (EG) Nr. 853/2004.</li>
            </ul>

            <h2>7. Nachhaltigkeit und Tierverwertung</h2>
            <p>Nutzen Sie jeden Teil des Tieres effizient:</p>
            <ul>
                <li>Hochwertige Fleischstücke für den Verkauf.</li>
                <li>Bindegewebsreiche Teile für Wurstwaren.</li>
                <li>Knochen und Sehnen für Brühen oder Gelatine.</li>
                <li><strong>Regionalität:</strong> Bevorzugen Sie Fleisch aus nachhaltiger, regionaler Tierhaltung.</li>
            </ul>

            <h2>8. Herausforderungen und Lösungen</h2>
            <ul>
                <li><strong>Herausforderung:</strong> Minimaler Fleischverlust.<br>
                    <strong>Lösung:</strong> Regelmäßiges Training der Schnittführung.
                </li>
                <li><strong>Herausforderung:</strong> Hygienemanagement.<br>
                    <strong>Lösung:</strong> HACCP-Systeme und Überwachung.
                </li>
                <li><strong>Herausforderung:</strong> Kundenwünsche.<br>
                    <strong>Lösung:</strong> Flexible Portionierung.
                </li>
            </ul>

            <h2>9. Fachliche Weiterentwicklung</h2>
            <p>Entwickeln Sie Ihre Fähigkeiten weiter:</p>
            <ul>
                <li><strong>Ausbildung:</strong> Fleischer/Metzger-Ausbildung mit Fokus auf Zerlegung.</li>
                <li><strong>Seminare:</strong> Kurse zu modernen Zerlegetechniken.</li>
                <li><strong>Literatur:</strong> Fachbücher zur Anatomie und Veredelung.</li>
            </ul>

            <h2>10. Innovationen im Bereich Zerlegung</h2>
            <ul>
                <li><strong>Automatisierung:</strong> Roboter für präzise Schnitte in Großbetrieben.</li>
                <li><strong>Digitalisierung:</strong> Rückverfolgbarkeit der Fleischstücke.</li>
                <li><strong>Nachhaltige Verpackung:</strong> Biologisch abbaubare Materialien.</li>
            </ul>
        </section>

