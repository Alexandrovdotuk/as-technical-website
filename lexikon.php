<script>
    // Suche
    document.getElementById('lexikonSearch').addEventListener('input', function(e) {
        let searchTerm = e.target.value.toLowerCase();
        let sections = document.querySelectorAll('.lexikon-content section');
        sections.forEach(section => {
            let content = section.textContent.toLowerCase();
            section.style.display = content.includes(searchTerm) ? 'block' : 'none';
        });
    });

    // Fix für Ankerlinks
    document.querySelectorAll('.application-box a, .modal-footer a').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            e.preventDefault();
            const targetId = this.getAttribute('href').substring(1);
            const targetElement = document.getElementById(targetId);
            if (targetElement) {
                targetElement.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });
</script>